package lt.pbp.domain;

public class Statistics {
	
	private double avgFreeSpaceByTime, avgFreeSpaceByCarsCount, avgLeaveInterval;

	public double getAvgFreeSpaceByTime() {
		return avgFreeSpaceByTime;
	}

	public void setAvgFreeSpaceByTime(double avgFreeSpaceByTime) {
		this.avgFreeSpaceByTime = avgFreeSpaceByTime;
	}

	public double getAvgFreeSpaceByCarsCount() {
		return avgFreeSpaceByCarsCount;
	}

	public void setAvgFreeSpaceByCarsCount(double avgFreeSpaceByCarsCount) {
		this.avgFreeSpaceByCarsCount = avgFreeSpaceByCarsCount;
	}

	public double getAvgLeaveInterval() {
		return avgLeaveInterval;
	}

	public void setAvgLeaveInterval(double avgLeaveInterval) {
		this.avgLeaveInterval = avgLeaveInterval;
	}
	
}
